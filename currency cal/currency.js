const URL =
  "https://cdn.jsdelivr.net/npm/@fawazahmed0/currency-api@latest/v1/currencies/";

let dropdowns = document.querySelectorAll(".dropdown select");

for (let select of dropdowns) {
  for (let code in countryList) {
    let newOption = document.createElement("option");
    newOption.innerText = code;
    newOption.value = code;
    select.append(newOption);
  }
  select.addEventListener("change", (evt) => {
    updateflag(evt.target);
  });
}

const updateflag = (element) => {
  let currcode = element.value;
  let countrycode = countryList[currcode];
  let newsrc = `https://flagsapi.com/${countrycode}/flat/64.png`;
  let img = element.parentElement.querySelector("img");
  img.src = newsrc;
};

const convert = () => {
  let submit = document.querySelector(".result button");
  submit.addEventListener("click", (evt) => {
    evt.preventDefault();

    let amount = document.querySelector(".amount input").value;
    let fromCurrency = document.querySelector("select[name='from']").value;
    let toCurrency = document.querySelector("select[name='to']").value;

    if (amount === "" || amount <= 0) {
      alert("Please enter a valid amount.");
      return;
    }

    let url = `${URL}${fromCurrency.toLowerCase()}.json`;

    fetch(url)
      .then((res) => res.json())
      .then((data) => {
        let rate = data[fromCurrency.toLowerCase()][toCurrency.toLowerCase()];
        let result = (amount * rate).toFixed(2);
        document.querySelector(".result input").value = result;
      })
      .catch((err) => {
        console.error("Error fetching data: ", err);
        alert("Something went wrong. Try again later.");
      });
  });
};

